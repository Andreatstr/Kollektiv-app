package model;

public class GeneratedWashingTable {
    private String task;
    private String name;

    public GeneratedWashingTable(){}

    public GeneratedWashingTable(String task, String name) {
        this.task = task;
        this.name = name;
    }

    public String getTask() {
        return task;
    }

    public void setTask(String task) {
        this.task = task;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
